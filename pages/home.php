<h3 class="lead">International Conference on Industrial Electrical and Electronics 2018</h3> <hr>
<p align="justify">Department of Electrical Engineering, University of Sultan Ageng Tirtayasa sincerely invite you
  to the International Conference on Industrial Electrical and Electronics (ICIEE 2018).
  The aim of this event is to provide an opportunity to present and share the most current research in
  Industrial, Electrical, Electronics, Information Technology, and Engineering Research. </p>

<p align="justify">The conference will be held in Aston Hotel, Anyer Beach, Banten, Indonesia on September 4-5th, 2018.
  We invite you to join hundreds of fellow researchers, academicians, scientists, and engineers in two days of
  innovation and creativity. Submit and share your best and original work related to the respective conference's topic.</p>

<p><strong>Date of Conference:</strong> September 4-5th, 2018</p><br>

<p><strong>Venue: </strong>Aston Hotel, Anyer Beach, Banten, Indonesia</p><br>

<p><strong>Theme: </strong>The opportunities, challenges, and future impacts of industry 4.0</p><br>

