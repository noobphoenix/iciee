<h3 class="lead">Template and Guidelines</h3> <hr>
<h4>Template</h4>
<p>Please follow these essential guidelines when preparing your paper:</p>
<ul class="bulleted">
<li><a href="http://cms.iopscience.iop.org/alfresco/d/d/workspace/SpacesStore/ac424614-d325-11e0-adda-5d01ae4695ed/simpleinstructions.pdf" target="_blank"><strong>Basic guidelines for preparing a paper</strong></a>&nbsp;</li>
<li><strong><a href="http://cms.iopscience.iop.org/alfresco/d/d/workspace/SpacesStore/999bfe76-32dc-11e6-a415-496188f8a415/References.pdf" target="_blank">Guidelines for preparing reference lists</a></strong></li>
</ul>
<p>Authors must prepare their papers using our Microsoft Word or LaTeX2e templates, and then convert these to PDF format for submission:</p>
<ul class="bulleted">
<li><a href="http://cms.iopscience.iop.org/alfresco/d/d/workspace/SpacesStore/f67538ae-18b2-11e4-831a-29411a5deefe/WordGuidelines.zip" target="_blank"><strong>Microsoft Word templates</strong></a></li>
<li><strong><a href="http://cms.iopscience.iop.org/alfresco/d/d/workspace/SpacesStore/a83f1ab6-cd8f-11e0-be51-5d01ae4695ed/LaTeXTemplates.zip" target="_blank">LaTeX2e class file</a></strong>&nbsp;</li>
</ul>
<p>Please download the Word/LaTeX templates for extensive guidelines on formatting your paper. As a summary, please ensure the following:</p>
<ul class="bulleted">
<li>Paper size is European A4.</li>
<li>Margins are 4 cm (top), 2.5 cm (left and right) and 2.7 cm (bottom).</li>
<li>The paper includes the author name and affiliation (full address including country).</li>
<li>There are no page numbers, or headers and footers, within the paper.</li>
<li>The PDF is free of formatting errors (e.g. corrupt equations, missing or low-resolution figures), since conversion from Word to PDF can introduce formatting errors.</li>
<li>Text is single spaced,&nbsp;<em>not</em>&nbsp;double spaced.</li>
<li>The PDF file is editable and&nbsp;<em>not</em>&nbsp;password protected.</li>
<li>All pages are portrait (landscape pages should be rotated).</li>
<li>Reference lists are checked for accuracy.&nbsp;<strong>References can only be linked via CrossRef if they are correct and complete</strong>.</li>
<li>Figures are placed within the text,&nbsp;<em>not</em>&nbsp;collected at the end of the document.</li>
<li>A thorough proofread is conducted to check the standard of English and ensure wording is clear and concise.</li>
</ul>
